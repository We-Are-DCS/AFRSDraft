export interface CandidateSignal {
  timestamp: string;
  signal: string;
  value: string | number | boolean;
  source: string;
}

export interface CDHRequest {
  endpoint: string;
  method: 'POST';
  headers: Record<string, string>;
  body: {
    DecisioningContext: {
      Channel: string;
      Direction: string;
      ContainerName: string;
      Touchpoint: string;
    };
    CustomerProfile: {
      CustomerID: string;
      Service: string;
      RolePreference: string;
      Cohort: string;
      Signals: CandidateSignal[];
    };
    Context: {
      PageURL?: string;
      SessionID: string;
      Timestamp: string;
    };
  };
}

export interface NBAAction {
  ActionID: string;
  ActionName: string;
  Treatment: string;
  Channel: string;
  Direction: string;
  pAccept: number;
  pValue: number;
  Rank: number;
  ContentFragmentID?: string;
  AudienceSegment?: string;
  // CDH live response display fields
  Label?: string;
  ShortDescription?: string;
  Benefits?: string;
  ImageURL?: string;
  // Video creative — CDH serves URL from media host
  videoURL?: string;
  videoThumbnailURL?: string;
  videoDuration?: string;
  videoTitle?: string;
  videoType?: 'reel' | 'short' | 'preroll' | 'story';
}

export interface CDHResponse {
  status: 200;
  latencyMs: number;
  ContainerName: string;
  CustomerID: string;
  DecisionTimestamp: string;
  AdaptiveModelScore: number;
  PropensityScore: number;
  Actions: NBAAction[];
  EngagementPolicyResult: {
    Eligible: boolean;
    Consented: boolean;
    FatigueCheck: 'PASS' | 'FAIL';
    DisparityRatio: number;
    ATRSRef: string;
  };
}

